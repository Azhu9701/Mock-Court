QUESTION = "滴滴外卖平台说自己是技术服务商不是雇主，你怎么看？请以你的身份和立场回答。"

def call_lmstudio(system_prompt: str, user_content: str, label: str):
    print(f"\n{'='*60}")
    print(f"📡 调用 LM Studio [{label}]...")
    print(f"{'='*60}")
    t0 = time.time()

    body = {
        "model": MODEL,
        "input": user_content,
        "temperature": TEMPERATURE,
        "max_output_tokens": MAX_TOKENS,
        "system_prompt": system_prompt,
        "stream": False
    }

    resp = requests.post(f"{BASE}/api/v1/chat", json=body, timeout=300)
    if resp.status_code != 200:
        print(f"❌ 失败: {resp.status_code} {resp.text[:300]}")
        return None

    data = resp.json()
    text = data.get("output", "")
    # Try alternative field names
    if not text:
        text = data.get("choices", [{}])[0].get("message", {}).get("content", "")
    if not text:
        text = data.get("response", "")
    if not text:
        text = str(data)[:500]

    elapsed = time.time() - t0
    print(f"⏱ {elapsed:.0f}s | 回复长度: {len(text)} 字符")
    print(f"\n--- 回复 ---\n{text}\n--- 结束 ---")
    return {"label": label, "text": text, "chars": len(text), "elapsed": elapsed}

